    event.preventDefault();
    var $modalContainer = $(this).closest('.portal-container');
    var $action = $modalContainer.find('.ph-1');

    if ($action.hasClass("closed")) {
    console.log('clicked');
      $action
        .removeClass("closed")
          .addClass("open");
    } else if ($action.hasClass("open")) {
      $action
        .removeClass("open")
          .addClass("closed");
    }
